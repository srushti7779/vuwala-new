    {
        return [
            [['id', 'user_id', 'status', 'create_user_id', 'update_user_id'], 'integer'],
            [['address', 'location', 'latitude', 'longitude', 'address_label', 'land_mark', 'created_on', 'updated_on', 'contact_no', 'contact_name'], 'safe'],
